(function($) {

	var	$window = $(window),
		$body = $('body');
		$nav = $('#nav');

		// Aplica la clase 'current' basada en la URL actual
		function updateNavCurrent() {
			var path = window.location.pathname;
	
			// Elimina la clase 'current' de todos los elementos de la lista
			$nav.find('ul li').removeClass('current');
	
			// Agrega la clase 'current' al elemento de la lista que corresponde a la URL actual
			$nav.find('ul li a').each(function() {
				var $this = $(this);
	
				if ($this.attr('href') === path) {
					$this.parent('li').addClass('current');
				}
			});
		}
	
		// Llama a la función de actualización en la carga de la página
		updateNavCurrent();
	// Breakpoints.
		breakpoints({
			wide:      [ '1281px',  '1680px' ],
			normal:    [ '981px',   '1280px' ],
			narrow:    [ '841px',   '980px'  ],
			narrower:  [ '737px',   '840px'  ],
			mobile:    [ '481px',   '736px'  ],
			mobilep:   [ null,      '480px'  ]
		});

	// Play initial animations on page load.
		$window.on('load', function() {
			window.setTimeout(function() {
				$body.removeClass('is-preload');
			}, 100);
		});

	// Dropdowns.
		$('#nav > ul').dropotron({
			offsetY: -15,
			hoverDelay: 0,
			alignment: 'center'
		});

	// Nav.

		// Bar.
			$(
				'<div id="titleBar">' +
					'<a href="#navPanel" class="toggle"></a>' +
					'<span class="title">' + $('#logo').html() + '</span>' +
				'</div>'
			)
				.appendTo($body);

		// Panel.
			$(
				'<div id="navPanel">' +
					'<nav>' +
						$('#nav').navList() +
					'</nav>' +
				'</div>'
			)
				.appendTo($body)
				.panel({
					delay: 500,
					hideOnClick: true,
					hideOnSwipe: true,
					resetScroll: true,
					resetForms: true,
					side: 'left',
					target: $body,
					visibleClass: 'navPanel-visible'
				});

})(jQuery);